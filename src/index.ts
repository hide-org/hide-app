import { app, BrowserWindow, session, dialog } from 'electron';
import * as path from 'path';
import * as fs from 'fs';

// Set up logging
const setupLogging = () => {
  const logPath = path.join(app.getPath('userData'), 'logs');
  if (!fs.existsSync(logPath)) {
    fs.mkdirSync(logPath, { recursive: true });
  }

  const logFile = path.join(logPath, `${new Date().toISOString().split('T')[0]}.log`);
  const logStream = fs.createWriteStream(logFile, { flags: 'a' });

  // Redirect console.log and console.error to file
  const originalLog = console.log;
  const originalError = console.error;

  console.log = (...args) => {
    const message = args.map(arg =>
      typeof arg === 'object' ? JSON.stringify(arg, null, 2) : arg
    ).join(' ');
    logStream.write(`[LOG ${new Date().toISOString()}] ${message}\n`);
    originalLog.apply(console, args);
  };

  console.error = (...args) => {
    const message = args.map(arg =>
      typeof arg === 'object' ? JSON.stringify(arg, null, 2) : arg
    ).join(' ');
    logStream.write(`[ERROR ${new Date().toISOString()}] ${message}\n`);
    originalError.apply(console, args);
  };

  // Log startup info
  console.log('App starting...', {
    version: app.getVersion(),
    electron: process.versions.electron,
    chrome: process.versions.chrome,
    node: process.versions.node,
    platform: process.platform,
    arch: process.arch,
    isPackaged: app.isPackaged,
    resourcesPath: process.resourcesPath,
    appPath: app.getAppPath(),
    userData: app.getPath('userData')
  });
};
// This allows TypeScript to pick up the magic constants that's auto-generated by Forge's Webpack
// plugin that tells the Electron app where to look for the Webpack-bundled app code (depending on
// whether you're running in development or production).
declare const MAIN_WINDOW_WEBPACK_ENTRY: string;
declare const MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY: string;

// Set up logging as early as possible
setupLogging();

// Catch any uncaught errors
process.on('uncaughtException', (error) => {
  console.error('Uncaught exception:', {
    error: error.toString(),
    stack: error.stack,
    details: error
  });
});

process.on('unhandledRejection', (reason, promise) => {
  const error = reason instanceof Error ? reason : new Error(String(reason));
  console.error('Unhandled rejection:', {
    error: error.toString(),
    stack: error.stack,
    details: reason
  });
});

// Handle creating/removing shortcuts on Windows when installing/uninstalling.
if (require('electron-squirrel-startup')) {
  app.quit();
}

const createWindow = (): void => {
  // Create the browser window.
  const mainWindow = new BrowserWindow({
    height: 900,
    width: 1440,
    // titleBarStyle: 'hiddenInset',  // Makes title bar blend with the app
    // backgroundColor: '#ffffff',     // Match your app's background
    // trafficLightPosition: { x: 20, y: 20 },
    webPreferences: {
      preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
      webSecurity: true,
    },
  });

  // Set up CSP
  session.defaultSession.webRequest.onHeadersReceived((details, callback) => {
    callback({
      responseHeaders: {
        ...details.responseHeaders,
        'Content-Security-Policy': [
          "default-src 'self' 'unsafe-inline' data:;" +
          "connect-src 'self' https://api.anthropic.com data:;" +
          "script-src 'self' 'unsafe-eval' 'unsafe-inline' data:;" +
          "style-src 'self' 'unsafe-inline' data:;"
        ]
      }
    });
  });

  // and load the index.html of the app.
  mainWindow.loadURL(MAIN_WINDOW_WEBPACK_ENTRY);
};

// This method will be called when Electron has finished
// initialization and is ready to create browser windows.
// Some APIs can only be used after this event occurs.
app.on('ready', createWindow);

// Quit when all windows are closed, except on macOS. There, it's common
// for applications and their menu bar to stay active until the user quits
// explicitly with Cmd + Q.
app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

app.on('activate', () => {
  // On OS X it's common to re-create a window in the app when the
  // dock icon is clicked and there are no other windows open.
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow();
  }
});

// Initialize MCP service
import { initializeMCP } from './main/mcp';

// TODO: Make this configurable
const getMCPConfig = async () => {
  const mcpPath = !app.isPackaged
    ? '/Users/artemm/Code/hide-mcp'  // Development
    : path.join(process.resourcesPath, 'hide-mcp');  // Production

  // Log some diagnostic information
  console.log('MCP configuration:', {
    resourcesPath: process.resourcesPath,
    mcpPath,
    exists: require('fs').existsSync(mcpPath),
    contents: require('fs').existsSync(mcpPath) ? require('fs').readdirSync(mcpPath) : 'N/A'
  });

  // Verify that the MCP directory exists
  if (!require('fs').existsSync(mcpPath)) {
    throw new Error(`MCP directory not found at ${mcpPath}`);
  }

  // In development, use system's uv
  if (!app.isPackaged) {
    return {
      cmd: 'uv',
      args: ['--directory', mcpPath, 'run', 'hide-mcp', 'server']
    };
  }

  // In production, use installed uv
  const uvPath = await ensureUV();
  console.log('Using uv configuration:', { uvPath, mcpPath });

  return {
    cmd: uvPath,
    args: [
      '--directory', mcpPath,  // Point to MCP directory
      'run',                   // uv will handle dependencies automatically
      'hide-mcp',
      'server'
    ]
  };
};

// Import the UV installer
import { ensureUV } from './main/install-uv';

app.whenReady().then(async () => {
  const { cmd, args } = await getMCPConfig();
  console.log('Initializing MCP...', { cmd, args });
  try {
    const initPromise = initializeMCP(cmd, args);

    // Create window immediately but show it only after MCP is ready
    createWindow();

    // Wait for MCP initialization
    await initPromise;
    console.log('MCP initialized successfully');
  } catch (err) {
    console.error('Failed to initialize MCP:', err);
    // Show an error dialog to the user
    dialog.showErrorBox(
      'Error Starting Application',
      'Failed to initialize required services. The application may not work correctly.'
    );
  }
});
